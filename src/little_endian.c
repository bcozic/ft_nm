#include "ft_nm_otool.h"

uint32_t	little_endian_32(uint32_t nbr)
{
	return (((nbr & 0x000000FF) << 24)
			| ((nbr & 0x0000FF00) << 8)
			| ((nbr & 0x00FF0000) >> 8)
			| ((nbr & 0xFF000000) >> 24));
}

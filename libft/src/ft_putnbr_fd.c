#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		neg;
	char	str[16];
	int		i;

	i = 15;
	neg = 1;
	str[i] = 0;
	if (n < 0)
		neg = -1;
	if (n >= 10 || n <= -10)
	{
		str[--i] = (char)((n % 10) * neg) + '0';
		n /= 10;
	}
	str[--i] = (char)((n % 10) * neg) + '0';
	if (neg == -1)
		str[--i] = '-';
	write(fd, str + i, (size_t)(15 - i));
}

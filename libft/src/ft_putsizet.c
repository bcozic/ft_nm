#include "libft.h"

void	ft_putsizet(size_t n)
{
	char	str[16];
	int		i;

	i = 15;
	str[i] = '\0';
	str[i - 1] = '0';
	while (n)
	{
		str[--i] = STR_HEXA[n % 16];
		n /= 16;
	}
	ft_putstr(str + i);
}
